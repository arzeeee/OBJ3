package maling.ayam.pkg2017;

/**
 *
 * @author Aya Aurora
 */
public class Alien {
    
    private int x;
    private int y;
    
    public Alien(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    void setX(int x) {
        this.x = x;
    }

    void setY(int y) {
        this.y = y;
    }
}
